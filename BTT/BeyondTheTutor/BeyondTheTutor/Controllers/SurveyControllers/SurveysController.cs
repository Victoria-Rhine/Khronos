using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using BeyondTheTutor.DAL;
using BeyondTheTutor.Models.SurveyModels;

namespace BeyondTheTutor.Controllers
{
    [Authorize]
    public class SurveysController : Controller
    {
        private BeyondTheTutorContext db = new BeyondTheTutorContext();

        // GET: Surveys
        public ActionResult Index()
        {
            ViewBag.Current = "ProfSurveyIndex";
            if (TempData["err"] != null)
            {
                ViewBag.Error = "Something went wrong! please try again later.";
                
            }

            return View(db.Surveys.Include(m => m.Questions).ToList());
        }

        // GET: Surveys/Create
        public ActionResult Create()
        {
            ViewBag.ClassID = new SelectList(db.Classes.OrderBy(m => m.Name), "ID", "Name");

            return View();
        }

        // POST: Surveys/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "ID,Name,Description,ClassID")] Survey survey)
        {
            if (ModelState.IsValid)
            {
                db.Surveys.Add(survey);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            TempData["err"] = "error";
            return RedirectToAction("Index");
        }


        // GET: Surveys/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Survey survey = db.Surveys.Find(id);
            if (survey == null)
            {
                return HttpNotFound();
            }
            ViewBag.Class = db.Classes.Find(survey.ClassID).Name;

            return View(survey);
        }

        // POST: Surveys/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Survey survey = db.Surveys.Find(id);
            db.Surveys.Remove(survey);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
